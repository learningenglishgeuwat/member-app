import type {
  SpeakingDomain,
  SpeakingGoalCard,
  SpeakingLevelBand,
  SurvivalPriority,
} from '../types';

type A1PhaseId = 'cefr-a1-1' | 'cefr-a1-2' | 'cefr-a1-3';

type GoalSeed = {
  domain: SpeakingDomain;
  survivalPriority: SurvivalPriority;
  goal: string;
  situation: string;
  keySentences: [string, string, string, string, string];
  keySentencesId?: [string, string, string, string, string];
  drill: string;
};

const A1_PHASE_META: Record<
  A1PhaseId,
  { phaseTitle: string; levelBand: SpeakingLevelBand }
> = {
  'cefr-a1-1': {
    phaseTitle: 'Survival Response',
    levelBand: 'starter',
  },
  'cefr-a1-2': {
    phaseTitle: 'Identity and Daily Needs',
    levelBand: 'starter',
  },
  'cefr-a1-3': {
    phaseTitle: 'Simple Transaction and Direction',
    levelBand: 'basic',
  },
};

function buildPhaseGoals(phaseId: A1PhaseId, seeds: GoalSeed[]): SpeakingGoalCard[] {
  const meta = A1_PHASE_META[phaseId];

  return seeds.map((seed, index) => ({
    id: `${phaseId}-g${String(index + 1).padStart(2, '0')}`,
    phaseId,
    phaseTitle: meta.phaseTitle,
    goalOrder: index + 1,
    domain: seed.domain,
    levelBand: meta.levelBand,
    survivalPriority: seed.survivalPriority,
    goal: seed.goal,
    situation: seed.situation,
    keySentences: seed.keySentences,
    keySentencesId: seed.keySentencesId,
    drill: seed.drill,
  }));
}

